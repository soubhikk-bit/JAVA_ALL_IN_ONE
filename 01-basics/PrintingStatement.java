public class PrintingStatement{
    public static void main(String[] args) {
        System.out.print("hello world");// it is used to print the value without moving the cursor to the next line.
        System.out.println(" my name is soubhik");// after print it moves the cursor to the next line.
        System.out.print("I \n love \n java");//to move the cursor to the nextline while printing the value we can use \n.
    }
}